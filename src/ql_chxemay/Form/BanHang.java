    private void btnHuyHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHuyHDActionPerformed
        // TODO add your handling code here:
        if(txtSoHD.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Không có hóa đơn nào để hủy!","Thông báo",1);
        }
        else
        {
            int sohd=Integer.parseInt(txtSoHD.getText());
            if(dao_HD.search(sohd)==null)
            {
                JOptionPane.showMessageDialog(this,"Hóa đơn không tồn tại!","Thông báo",1);
                data_CTHD.removeAllElements();
                tableCTHD.setModel(new DefaultTableModel(data_CTHD,header_CTHD));
            }
            else if(dao_CTHD.kTraTonTai(sohd)!=1)
            {
                dao_HD.delete(sohd);
                JOptionPane.showMessageDialog(this,"Thành Công!","Thông báo",1);
            }
            else if(dao_CTHD.huyHoaDon(sohd)==1)
            {
                JOptionPane.showMessageDialog(this,"Thành Công!","Thông báo",1);
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Lỗi!","Thông báo",1);
            }
            layDuLieuCTHD(sohd);
            loadDataTabKhoXe();
            loadThongTinLSB();

        }
        txtSoHD.setText("");
        cbMaNV.setSelectedItem("");
        cbMaKH.setSelectedIndex(-1);
        txtTenKH.setText("");
        txtNgayTao.setText("");
        txtTLGiamGia.setText("");
        txtTongTien.setText("");
        txtTrangThai.setText("");
        txtGhiChu.setText("");

    }//GEN-LAST:event_btnHuyHDActionPerformed

